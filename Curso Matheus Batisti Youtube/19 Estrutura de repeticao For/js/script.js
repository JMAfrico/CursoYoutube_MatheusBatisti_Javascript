//Inicialização da variável
//Limite da variável, condição para repetir
//Enquanto x for menor que 5, repita
var x;
for(x = 0 ; x <= 5; x++){
    console.log(x);
}

var nome = "Joao";
for(y = 0 ; y <= nome.length; y++){
    console.log(nome[y]);
}

var arr = ["Joao","Elen","Mae","Pai"];
for(z = 0 ; z <= arr.length; z++){
    console.log(arr[z]);
}
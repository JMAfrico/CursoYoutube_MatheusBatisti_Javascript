//18 Estrutura de repeticao While
// while(condição){}

//Inicialização da variável
var x = 0;

//Limite da variável, condição para repetir
//Enquanto x for menor que 5, repita
while(x < 5){
    //Lógica que altera o valor de x
    x = x+1
    console.log(x);
}

var arr= ['teste','testando','1','2'];
var y=0;
while(y <= arr.length){
    console.log(arr[y]);
    y++;
}

var palavra = "João";
var z=0;
while(z <= palavra.length){
    console.log(palavra[z]);
    z++;
}